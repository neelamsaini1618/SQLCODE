
CREATE DATABASE UNIVERSITY

---UNDER THIS UNIVERSITY CREATE FOUR TABLES AND EACH TABLE SHOULD HAVE FOLLOWING THREE COLUMN NAMES AS

CREATE TABLE COLLEGE
(
COLLEGE_ID INT PRIMARY KEY,
COLLEGE_NAME VARCHAR(50) NOT NULL,
COLLEGE_AREA VARCHAR(100) NOT NULL
)

CREATE TABLE DEPARTMENT
(
DEPT_ID INT PRIMARY KEY,
DEPT_NAME VARCHAR(40) NOT NULL,
DEPT_FACILITY VARCHAR(60)
)

CREATE TABLE PROFESSOR
(
PROFESSOR_ID INT PRIMARY KEY,
PROFESSOR_NAME VARCHAR(30)  NOT NULL,
PROFESSOR_SUBJECT VARCHAR(40) NOT NULL
)

CREATE TABLE STUDENT
(
STUDENT_ID INT PRIMARY KEY,
STUDENT_NAME VARCHAR(50) NOT NULL,
STUDENT_STREAM VARCHAR(60)
)

----APPLY FOREGIN KEY ON DEPARTMENT TABLE FROM COLLEGE_TABLE

ALTER TABLE DEPARTMENT ADD CONSTRAINT FK_COLLEGEDEPARTMENT FOREIGN KEY (DEPT_ID) REFERENCES  COLLEGE(COLLEGE_ID)

---APPLY FOREGIN KEY ON STUDENT_TABLE FROM PROFESSOR_TABLE

ALTER TABLE STUDENT ADD CONSTRAINT FK_STUDENTPROFESSOR  FOREIGN KEY (STUDENT_ID) REFERENCES PROFESSOR(PROFESSOR_ID)

